# Numeric Types
print(10)           # int
print(3.14)         # float
print(2 + 3j)       # complex

# Sequence Types
print("Hello, Python!")     # str
print([1, 2, 3, 4, 5])      # list
print((10, 20, 30))         # tuple

# Mapping Type
print({'name': 'Alice', 'age': 30})   # dict

# Set Types
print({1, 2, 3, 4, 5})      # set
print(frozenset([10, 20, 30]))       # frozenset

# Boolean Type
print(True)         # bool

# None Type
print(None)         # NoneType